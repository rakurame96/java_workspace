public class DataTypes {
    public static void main(String[] args)
    {
        int int_a = 5;
        byte byte_b = 127;
        short short_c = 32767;
        long long_d = 56545l;
        float float_e = 6.5f;
        double double_f = 5.5478996;
        boolean bool = true;
        char char_g = 'r';

        System.out.println(int_a);
        System.out.println(byte_b);
        System.out.println(short_c);
        System.out.println(long_d);
        System.out.println(float_e);
        System.out.println(double_f);
        System.out.println(bool);
        System.out.println(char_g);
    }
}
